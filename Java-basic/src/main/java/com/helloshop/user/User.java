package com.helloshop.user;

public class User {
    private String userId;
    private String name;

    // getter/setter 메서드 추가
    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}